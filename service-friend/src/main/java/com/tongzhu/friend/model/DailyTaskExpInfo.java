package com.tongzhu.friend.model;

public class DailyTaskExpInfo {
    private Integer level;

    private Integer primaryExp;

    private Integer intermediateExp;

    private Integer advancedExp;

    public Integer getLevel() {
        return level;
    }

    public void setLevel(Integer level) {
        this.level = level;
    }

    public Integer getPrimaryExp() {
        return primaryExp;
    }

    public void setPrimaryExp(Integer primaryExp) {
        this.primaryExp = primaryExp;
    }

    public Integer getIntermediateExp() {
        return intermediateExp;
    }

    public void setIntermediateExp(Integer intermediateExp) {
        this.intermediateExp = intermediateExp;
    }

    public Integer getAdvancedExp() {
        return advancedExp;
    }

    public void setAdvancedExp(Integer advancedExp) {
        this.advancedExp = advancedExp;
    }
}