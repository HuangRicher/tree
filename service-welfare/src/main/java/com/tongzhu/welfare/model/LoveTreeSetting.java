package com.tongzhu.welfare.model;

public class LoveTreeSetting {
    private Integer grade;

    private Integer expLimit;

    private Integer attack;

    private Integer spellAttacks;

    private Integer pdef;

    private Integer magdef;

    private Integer vitality;

    public Integer getGrade() {
        return grade;
    }

    public void setGrade(Integer grade) {
        this.grade = grade;
    }

    public Integer getExpLimit() {
        return expLimit;
    }

    public void setExpLimit(Integer expLimit) {
        this.expLimit = expLimit;
    }

    public Integer getAttack() {
        return attack;
    }

    public void setAttack(Integer attack) {
        this.attack = attack;
    }

    public Integer getSpellAttacks() {
        return spellAttacks;
    }

    public void setSpellAttacks(Integer spellAttacks) {
        this.spellAttacks = spellAttacks;
    }

    public Integer getPdef() {
        return pdef;
    }

    public void setPdef(Integer pdef) {
        this.pdef = pdef;
    }

    public Integer getMagdef() {
        return magdef;
    }

    public void setMagdef(Integer magdef) {
        this.magdef = magdef;
    }

    public Integer getVitality() {
        return vitality;
    }

    public void setVitality(Integer vitality) {
        this.vitality = vitality;
    }
}