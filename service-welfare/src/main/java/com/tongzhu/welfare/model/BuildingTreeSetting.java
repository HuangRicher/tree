package com.tongzhu.welfare.model;

public class BuildingTreeSetting {
    private Integer id;

    private Integer grade;

    private Long ambienceCount;

    private Long amusementCount;

    private Long environmentCount;

    private Integer upgradeGold;

    private Integer upgradeTime;

    private Integer containNum;

    private Integer containThing;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getGrade() {
        return grade;
    }

    public void setGrade(Integer grade) {
        this.grade = grade;
    }

    public Long getAmbienceCount() {
        return ambienceCount;
    }

    public void setAmbienceCount(Long ambienceCount) {
        this.ambienceCount = ambienceCount;
    }

    public Long getAmusementCount() {
        return amusementCount;
    }

    public void setAmusementCount(Long amusementCount) {
        this.amusementCount = amusementCount;
    }

    public Long getEnvironmentCount() {
        return environmentCount;
    }

    public void setEnvironmentCount(Long environmentCount) {
        this.environmentCount = environmentCount;
    }

    public Integer getUpgradeGold() {
        return upgradeGold;
    }

    public void setUpgradeGold(Integer upgradeGold) {
        this.upgradeGold = upgradeGold;
    }

    public Integer getUpgradeTime() {
        return upgradeTime;
    }

    public void setUpgradeTime(Integer upgradeTime) {
        this.upgradeTime = upgradeTime;
    }

    public Integer getContainNum() {
        return containNum;
    }

    public void setContainNum(Integer containNum) {
        this.containNum = containNum;
    }

    public Integer getContainThing() {
        return containThing;
    }

    public void setContainThing(Integer containThing) {
        this.containThing = containThing;
    }
}