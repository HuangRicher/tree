package com.tongzhu.usergoods.model;

public class WeddingRingUpgradeInfo {
    private Integer level;

    private Double attack;

    private Double vitality;

    private Double pdef;

    private Double magdef;

    private Double spellAttacks;

    private Long happinessNum;

    public Integer getLevel() {
        return level;
    }

    public void setLevel(Integer level) {
        this.level = level;
    }

    public Double getAttack() {
        return attack;
    }

    public void setAttack(Double attack) {
        this.attack = attack;
    }

    public Double getVitality() {
        return vitality;
    }

    public void setVitality(Double vitality) {
        this.vitality = vitality;
    }

    public Double getPdef() {
        return pdef;
    }

    public void setPdef(Double pdef) {
        this.pdef = pdef;
    }

    public Double getMagdef() {
        return magdef;
    }

    public void setMagdef(Double magdef) {
        this.magdef = magdef;
    }

    public Double getSpellAttacks() {
        return spellAttacks;
    }

    public void setSpellAttacks(Double spellAttacks) {
        this.spellAttacks = spellAttacks;
    }

    public Long getHappinessNum() {
        return happinessNum;
    }

    public void setHappinessNum(Long happinessNum) {
        this.happinessNum = happinessNum;
    }
}