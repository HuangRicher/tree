package com.tongzhu.treehouse.model;

public class FlowerNumberSetting {
    private Integer dayPlantCount;

    private Float probability;

    public Integer getDayPlantCount() {
        return dayPlantCount;
    }

    public void setDayPlantCount(Integer dayPlantCount) {
        this.dayPlantCount = dayPlantCount;
    }

    public Float getProbability() {
        return probability;
    }

    public void setProbability(Float probability) {
        this.probability = probability;
    }
}